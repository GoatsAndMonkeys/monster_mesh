int drawChar(SDL_Renderer *r, int x, int y, char ch, SDL_Color fg, int scale) {
    if (scale < 1) scale = 1;
    unsigned char uch = (unsigned char)ch;
    if (uch < 0x20 || uch > 0x7F) {
        // Unsupported — render a hollow box so missing glyphs are visible.
        SDL_SetRenderDrawColor(r, fg.r, fg.g, fg.b, fg.a);
        SDL_Rect rc = { x, y, GLYPH_W * scale, GLYPH_H * scale };
        SDL_RenderDrawRect(r, &rc);
        return GLYPH_ADVANCE * scale;
    }
    const uint8_t *g = kGlyphs[uch - 0x20];
    SDL_SetRenderDrawColor(r, fg.r, fg.g, fg.b, fg.a);
    for (int row = 0; row < GLYPH_H; row++) {
        uint8_t bits = g[row];
        for (int col = 0; col < GLYPH_W; col++) {
            if (bits & (0x10 >> col)) {
                SDL_Rect rc = { x + col * scale, y + row * scale, scale, scale };
                SDL_RenderFillRect(r, &rc);
            }
        }
    }
    return GLYPH_ADVANCE * scale;
}

int drawString(SDL_Renderer *r, int x, int y, const char *s, SDL_Color fg, int scale) {
    if (!s) return 0;
    int cx = x;
    for (; *s; ++s) cx += drawChar(r, cx, y, *s, fg, scale);
    return cx - x;
}

int stringWidth(const char *s, int scale) {
    if (!s) return 0;
    if (scale < 1) scale = 1;
    return (int)strlen(s) * GLYPH_ADVANCE * scale;
}

